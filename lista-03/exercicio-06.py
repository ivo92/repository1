cf = input("Digite o seu conceito final: ")

if cf == "A+":
    print("Seu conceito final foi Excepcional")

elif cf == "A":
    print("Seu conceito final foi Excelente ")

elif cf == "A-":
    print("Seu conceito final foi Excelente")

elif cf == "B+":
    print("Seu conceito final foi Bom")

elif cf == "B":
    print("Seu conceito final foi Bom")

elif cf == "B-":
    print("Seu conceito final foi Bom")

elif cf == "C+":
    print("Seu conceito final foi Regular")

elif cf == "C":
    print("Seu conceito final foi Regular")

elif cf == "C-":
    print("Seu conceito final foi Regular")

elif cf == "D":
    print("Seu conceito final foi Reprovação")

else:
    print("Você não digitou um conceito válido. Rode o programa novamente.")